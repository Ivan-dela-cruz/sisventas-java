    private void LoadAdmin() {
        try {
            Table_admin.removeAll();
            String[] titulos = {"id", "Ci", "Nombres", "Apellidos", "Dirección", "Teléfono"};
            String[] registro = new String[6];
            Object o[] = null;
            List<Administrador> listAdmin = adminController.findAdministradorEntities();
            modelo2 = new DefaultTableModel(null, titulos);

            for (int i = 0; i < listAdmin.size(); i++) {
//                modelo2.addRow(o);
                registro[0] = String.valueOf(listAdmin.get(i).getId());
                registro[1] = listAdmin.get(i).getCi();
                registro[2] = listAdmin.get(i).getNombres();
                registro[3] = listAdmin.get(i).getApellidos();
                registro[4] = listAdmin.get(i).getDireccion();
                registro[5] = listAdmin.get(i).getTelefono();
                modelo2.addRow(registro);
            }
            Table_admin.setModel(modelo2);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "error2");
        }

    }

