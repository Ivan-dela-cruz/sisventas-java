    private void Button_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_EditarActionPerformed
        try {
            if (validar() == 5) {
                
                Administrador admin = adminController.findAdministrador(ids);
                admin.setCi(id);

                admin.setNombres(nombre);

                admin.setApellidos(apellido);

                admin.setDireccion(direccion);

                admin.setTelefono(telefono);
                adminController.edit(admin);

                JOptionPane.showMessageDialog(null, "Actualizado exitosamente");
                limpiar();
                this.id = "";
                this.nombre = "";
                this.apellido = "";
                this.direccion = "";
                this.telefono = "";
                LoadAdmin();
                this.Button_GuardarCliente.setEnabled(true);
                this.Button_Editar.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Complete todos los campos");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_Button_EditarActionPerformed

