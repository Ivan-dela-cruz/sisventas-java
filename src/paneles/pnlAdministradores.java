    private void saveAdministrador() {
        Administrador admin = new Administrador();

        admin.setCi(id);

        admin.setNombres(nombre);

        admin.setApellidos(apellido);

        admin.setDireccion(direccion);

        admin.setTelefono(telefono);

        adminController.create(admin);
        LoadAdmin();
    }

    private int validar() {
        int val = 0;
        if (TextField_idAdmin.getText().equals("")) {
            Label_IdCliente.setText("Ingrese la ID");
            Label_IdCliente.setForeground(Color.red);
            TextField_idAdmin.requestFocus();
        } else {
            val++;
            if (TextField_NombreAdmin.getText().equals("")) {
                Label_NombreCliente.setText("Ingrese el nombre");
                Label_NombreCliente.setForeground(Color.red);
                TextField_NombreAdmin.requestFocus();
            } else {
                val++;
                if (TextField_ApellidioAdmin.getText().equals("")) {
                    Label_ApellidoCliente.setText("Ingrese el apellido");
                    Label_ApellidoCliente.setForeground(Color.red);
                    TextField_ApellidioAdmin.requestFocus();
                } else {
                    val++;
                    if (TextField_DireccioAdmin.getText().equals("")) {
                        Label_DireccionCliente.setText("Ingrese la dirección");
                        Label_DireccionCliente.setForeground(Color.red);
                        TextField_DireccioAdmin.requestFocus();
                    } else {
                        val++;
                        if (TextField_TelefonoAdmin.getText().equals("")) {
                            Label_TelefonoCliente.setText("Ingrese el teléfono");
                            Label_TelefonoCliente.setForeground(Color.red);
                            TextField_TelefonoAdmin.requestFocus();
                        } else {
                            val++;
                            if (acction.equals("insert")) {
                                id = TextField_idAdmin.getText();
                                nombre = TextField_NombreAdmin.getText().toUpperCase();
                                apellido = TextField_ApellidioAdmin.getText().toUpperCase();
                                direccion = TextField_DireccioAdmin.getText().toUpperCase();
                                telefono = TextField_TelefonoAdmin.getText();
                            }

                        }

                    }

                }

            }
        }
        return val;
    }

