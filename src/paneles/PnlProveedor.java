    private void LoadDates() {
        try {
            Table_Proveedores.removeAll();
            String[] titulos = {"id", "Ruc", "Ci", "Nombres", "Apellidos", "Dirección", "Teléfono", "Email", "Empresa"};
            String[] registro = new String[9];
            //Object o[] = null;
            List<Proveedor> listproveedor = proveedorController.findProveedorEntities();
            modelo2 = new DefaultTableModel(null, titulos);

            for (int i = 0; i < listproveedor.size(); i++) {
//                modelo2.addRow(o);
                registro[0] = String.valueOf(listproveedor.get(i).getId());
                registro[1] = listproveedor.get(i).getRucProveedor();
                registro[2] = listproveedor.get(i).getCiProveedor();
                registro[3] = listproveedor.get(i).getNomProveedor();
                registro[4] = listproveedor.get(i).getApeProveedor();
                registro[5] = listproveedor.get(i).getDirProveedor();
                registro[6] = listproveedor.get(i).getTelefono();
                registro[7] = listproveedor.get(i).getEmail();
                registro[8] = listproveedor.get(i).getEmpresaProveedor();

                //System.out.println(listLocal.get(i).getAdmin().toString());
                modelo2.addRow(registro);
            }
            Table_Proveedores.setModel(modelo2);

        } catch (Exception e) {
            System.out.println(e.toString());
        }

    }

