    private int validar() {
        int val = 0;
        if (TextField_CiProveedor.getText().equals("")) {
            Label_ciProveedor.setText("Ingrese CI");
            Label_ciProveedor.setForeground(Color.red);
            TextField_CiProveedor.requestFocus();
        } else {
            val++;
            if (TextField_NombreProveedor.getText().equals("")) {
                Label_NombreProveedor.setText("Ingrese el nombre");
                Label_NombreProveedor.setForeground(Color.red);
                TextField_NombreProveedor.requestFocus();
            } else {
                val++;
                if (TextField_ApellidioProveedor.getText().equals("")) {
                    Label_ApellidoProveedor.setText("Ingrese el apellido");
                    Label_ApellidoProveedor.setForeground(Color.red);
                    TextField_ApellidioProveedor.requestFocus();
                } else {
                    val++;
                    if (TextField_DireccionProveedor.getText().equals("")) {
                        Label_DireccionProveedor.setText("Ingrese la dirección");
                        Label_DireccionProveedor.setForeground(Color.red);
                        TextField_DireccionProveedor.requestFocus();
                    } else {
                        val++;
                        if (TextField_TelefonoProveedor.getText().equals("")) {
                            Label_TelefonoProveedor.setText("Ingrese el teléfono");
                            Label_TelefonoProveedor.setForeground(Color.red);
                            TextField_TelefonoProveedor.requestFocus();
                        } else {
                            val++;
                            if (TextField_empresaProveedor.getText().equals("")) {
                                Label_empresaProveedor.setText("Ingrese nombre de la empresa");
                                Label_empresaProveedor.setForeground(Color.red);
                                TextField_empresaProveedor.requestFocus();
                            } else {
                                val++;
                                if (acction.equals("insert")) {
                                    ci = TextField_CiProveedor.getText();
                                    ruc = TextField_rucProveedor.getText();
                                    nombre = TextField_NombreProveedor.getText().toUpperCase();
                                    apellido = TextField_ApellidioProveedor.getText().toUpperCase();
                                    direccion = TextField_DireccionProveedor.getText().toUpperCase();
                                    telefono = TextField_TelefonoProveedor.getText();
                                    email = TextField_EmailProveedor.getText();
                                    empresa = TextField_empresaProveedor.getText().toUpperCase();

                                }

                            }

                        }

                    }

                }

            }
        }

        return val;
    }

