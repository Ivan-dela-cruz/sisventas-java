    public void actualizar() {
        System.out.println("valor de validar ; "+validar());
        if (validar() < 6) {

            ErrorAlert er = new ErrorAlert(new JFrame(), true);
            er.titulo.setText("OOPS...");
            er.msj.setText("FALTAN CAMPOS DE LLENAR");
            er.msj1.setText("");
            er.setVisible(true);

        } else {
            //String ci, String nom, String ruc, String dir, String ape, String telf
            Sentencias s = new Sentencias(apellido, ci, direccion, email, empresa,nombre,ruc,telefono);
            s.setId(this.ids);

            int opcion = proveedores.Opciones.actualizar(s);
            if (opcion != 0) {

                proveedores.Opciones.listarProveedor("");

                SuccessAlert sa = new SuccessAlert(new JFrame(), true);
                sa.titulo.setText("¡HECHO!");
                sa.msj.setText("SE HAN GUARDADO LOS CAMBIOS");
                sa.msj1.setText("");
                sa.setVisible(true);
            } else {
                System.out.println(s.toString());
            }
        }

    }
    
